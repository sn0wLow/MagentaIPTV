namespace MagentaIPTV
{
    /// <summary>
    /// Styles of page animations for appearing/disappearing
    /// </summary>
    public enum PageAnimation
    {
        /// <summary>
        /// No animation takes place
        /// </summary>
        None = 0,

        /// <summary>
        /// The page fades in
        /// </summary>
        FadeIn = 1,

        /// <summary>
        /// The page fades out
        /// </summary>
        FadeOut = 2,

        /// <summary>
        /// The page slides in from the right
        /// </summary>
        SlideInFromRight = 3,

        /// <summary>
        /// The page slides in from the left
        /// </summary>
        SlideInFromLeft = 4,

        /// <summary>
        /// The page slides out to the right
        /// </summary>
        SlideOutToRight = 5,

        /// <summary>
        /// The page slides out to the left
        /// </summary>
        SlideOutToLeft = 6

    }
}
